package Lab5.MovieStuff;


/**
 * The type Coordinates.
 */
public class Coordinates {
    private int x; //Максимальное значение поля: 906, Поле не может быть null
    private float y; //Поле не может быть null

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public float getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(float y) {
        this.y = y;
    }
}
